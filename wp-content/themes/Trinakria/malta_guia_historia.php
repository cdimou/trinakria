<h1>La historia de Malta</h1>
                             
<p>Malta es uno de los países más pequeños de Europa, sin embargo, su situación en el centro del Mediterráneo entre Italia, Túnez y Libia ha hecho de las islas un cruce de culturas y un destino turístico muy atractivo para muchos viajeros españoles. <a href="<?php echo site_url();?>/malta/rutas">Viajar a Malta</a> permite disfrutar de los sabores más auténticos del sur de Europa y de África. Todo con un clima excelente y un toque muy británico. El cóctel maltés combina bares y animación; una rica cultura y tradiciones; museos y monumentos; y una gastronomía que como el resto del país es una síntesis de Europa, Asia y África. Sin olvidar las playas y el mar azul que siempre brilla. Para ir abriendo boca y animaos a planificar la visita de Malta, os proponemos una pequeña guía de viaje.</p>

<p><font color="#FF7F00">Malta y Gozo</font>, son junto a <font color="#FF7F00">Comino</font> las tres islas del archipiélago que forma Malta. Un pequeño país, anclado entre África, Asía y Europa, pero con una larga e interesante historia. Hoy las influencias árabes, italianas y británicas se han mezclado en un panorama muy Mediterráneo, abierto y atractivo.</p>

<p>Malta ocupa menos de 316 km 2, con una población de unos 450.000 habitantes. La capital es <font color="#FF7F00">La Valletta</font>, situada al oeste de Malta. Junto a <font color="#FF7F00">Sliema</font>, <font color="#FF7F00">Paceville</font>, <font color="#FF7F00">St Julian's</font> y las tres ciudades de <font color="#FF7F00">Cottonera (Bormla, Birgu y L’Isla)</font> forma una área urbana que concentra la población del país. <font color="#FF7F00">Mdina</font>, <font color="#FF7F00">Mosta</font> y <font color="#FF7F00">Victoria/Rabat</font> (en Gozo) son las otras poblaciones importantes. La isla de Gozo es menos turística y más tranquila pero igual de interesante.</p> 

<p><font color="#FF7F00">El maltés y el inglés</font> son los idiomas oficiales, pero la población comprende y suele hablar el italiano. No es raro que los malteses hablen también francés y español.</p>

<p>Para leer más ir a <a href="http://islamalta.es/" target="blank">nuestro Blog</a> o en la página de <a href="http://es.wikipedia.org/wiki/Malta" target="blank">Wikipedia</a>. </p>

<p>Ahora las fechas históricas más importantes Malta:</p>

<p class="text-muted"><strong>5200-725 a.C.</strong></p>

<p>Como en otras islas del Mediterráneo el poblamiento humano comienza hace siete mil años, durante el <font color="#FF7F00">Neolítico</font>. En el tercer milenio a. C. se desarrolla una rica y desconocida cultura, caracterizada por <font color="#FF7F00"><em>los templos megalíticos</em></font>, como el de <font color="#FF7F00">Hagar Qim</font>, que son hoy uno de los principales monumentos de Malta.</p>

<p class="text-muted"><strong>725-218 a.C.  </strong></p>

<p>Primero <font color="#FF7F00">los fenicios</font>, después los griegos ocupan las islas. Los griegos establecen una colonia con capital en <font color="#FF7F00">Mdina</font>, con un régimen democrático. En el siglo IV a.C. serán <font color="#FF7F00">los cartagineses</font> quienes dominen Malta.</p>

<p class="text-muted"><strong>218 a.C -455.</strong></p>

<p><font color="#FF7F00"><em>Imperio Romano</em></font>. Malta se convierte en un punto estratégico de las guerras Púnicas hasta la conquista romana en 218 a.C La influencia romana será larga e intensa, produciendo una romanización profunda de los habitantes de Malta. </p>

<p class="text-muted"><strong>455-533. </strong></p>

<p><font color="#FF7F00"><em>Invasiones Bárbaras</em></font>. La caída del Imperio Romano afectó a las islas que serán conquistadas por Vándalos y Ostrogodos.</p>

<p class="text-muted"><strong>533-880. </strong></p>

<p><font color="#FF7F00"><em>Imperio Bizantino</em></font>. La expansión del Imperio Romano de Oriente con Justiniano llega a Malta en 533, de manos de su general más prestigioso, Belisario. </p>

<p class="text-muted"><strong>870-1091. </strong></p>

<p>Influencia árabe. <font color="#FF7F00"><em>Los árabes</em></font> ponen fin a la época bizantina y añaden una nueva civilización a la ya agitada Malta. La influencia árabe quedará en la lengua, la gastronomía y la agricultura.</p>

<p class="text-muted"><strong>1091-1530. </strong></p>
<p>La conquista normanda de Sicilia hace que Malta cambie de manos. <font color="#FF7F00"><em>El Conde Roger de Hauteville</em></font> expulsa a los árabes. <font color="#FF7F00"><em>La influencia siciliana</em></font> marcará, como la árabe, los rasgos de Malta, con el catolicismo y el modelo feudal. El italiano de Sicilia influirá en el maltés que mantiene sus raíces árabes pero que cambia de alfabeto.</p>

<p class="text-muted"><strong>1530-1798. </strong></p>

<p>Esta fecha marca otro de lo momentos importantes de la historia maltesa, la llegada de <font color="#FF7F00"><em>los caballeros de la Orden de San Juan</em></font>. Carlos I cede la soberanía de la isla a la Orden que la ha conquistado, para utilizarla como base avanzada contra los piratas berberiscos y los otomanos. Dependiente del Reino de Sicilia, a su vez territorio del Imperio Habsburgo, pero autónoma en la práctica, la presencia de los caballeros dejará una huella profunda en la arquitectura militar, la organización social y la memoria.</p>

<p>En 1565 tiene lugar <font color="#FF7F00"><em>el Gran Sitio de Malta</em></font>, un acontecimiento utilizado aún como hecho formador de identidad. Los otomanos intentan invadir Malta y el <font color="#FF7F00"><em>Gran Maestre La Vallette</em></font>, se atrinchera en la zona de las tres ciudades de Cottonera. Tras varios meses de sitio, la llegada de la flota española salva Malta y a los caballeros. Al año siguiente, en 1566, La Vallette funda una nueva capital que llevará su nombre. Apoyados por las coronas católicas de Europa y con miembros de todos los países, la Orden será un aliado importante en las victorias de Lepanto (1571) y Damietta (1732).</p>

<p>La pertenencia a la Orden de Malta será durante la Edad Moderna un elemento de prestigio, muchas veces una especie de entrenamiento y aprendizaje para numerosos nobles que servirán a la Orden y a sus reinos de origen, por ejemplo en Francia, España o los diversos reinos italianos.</p>

<p class="text-muted"><strong>1798 - 1800.</strong></p>

<p>El sistema feudal y la preeminencia de la aristocracia crea tensiones sociales como en el resto de Europa. Así <font color="#FF7F00">la Revolución Francesa</font> agitará Malta. Primero con una revuelta que fracasa por muy poco en 1793, después con la ocupación incruenta de Napoleón en 1798. Para éste, la importancia de Malta es estratégica en sus anhelos de conquista de Egipto.</p>

<p class="text-muted"><strong>1800 – 1964.</strong></p>

<p>El gobierno francés no es popular por su doble faz y ciertas medidas liberalizadoras. Sin embargo, es un nuevo sitio, está vez británico, el que desaloja a los franceses tras la derrota de Abukir en Egipto. </p>

<p><font color="#FF7F00"><em>Los británicos</em></font> abren el último gran periodo de configuración de la actual Malta. En lugar de devolver la isla a la Orden de Malta, Gran Bretaña decide anexarla como colonia, y hacer del archipiélago uno de los relais más importantes para su flota en el Mediterráneo, junto a Gibraltar.</p>

<p>La presencia británica tampoco es bien acogida por los intereses que defiende, alejados de los de la población. <font color="#FF7F00"><em>El inglés desplaza al italiano</em></font> como lengua, pero esto paradójicamente hará que los movimientos identitarios malteses se focalicen sobre el maltés.</p>

<p>En el XIX, progresivamente el gobierno británico concede más autonomía a la colonia. Aún así, en el XX se producen revueltas en los años 1930, lo que conduce a mayor autogobierno y el definitivo abandono del italiano.</p>

<p><font color="#FF7F00"><em>La Segunda Guerra Mundial</em></font> supone un receso en esta situación ya que Malta sufre un nuevo gran sitio que ilustrará a británicos y malteses en la defensa de la isla contra el fascismo y el nazismo. Bombardeada durante años, casi aislada, faltando de municiones y alimentos, Malta resiste como un portaviones en mitad del Imperio del Eje. Los aviones estacionados en Malta y la flota serán muy importantes en las campañas de Grecia, en la guerra del desierto libio y en la liberación del norte de África e Italia.</p>

<p>Tras la guerra, Malta obtiene la autonomía para los asuntos internos en 1947, después el status de Dominio (1955). Con todo, el Parlamento maltés declara unilateralmente la independencia en 1962.</p>

<p class="text-muted"><strong>1964-1974.</strong></p>

<p>Es, sin embargo, <font color="#FF7F00"><em>en 1964 cuando la independencia tiene lugar</em></font>, aunque manteniéndose en la Commonwealth. Época de flujos migratorios hacia Australia, Gran Bretaña y Canadá.</p>

<p class="text-muted"><strong>1974-2004.</strong></p>

<p>En este año se declara la República y comienza una política de estricta neutralidad, con relaciones muy estrechas con los países cercanos, sobre todo Italia y el régimen de Gadafi en Libia.</p>

<p class="text-muted"><strong>2004- </strong></p>

<p>En 2004 <font color="#FF7F00"><em>Malta entra la Unión Europea</em></font>, orientando la política interna y externa hacia Europa, está vez, de la que se convierte en frontera sur. El turismo se convierte en un importante sector económico del país.</p>
