        <h1>La Cocina de Cerdeña</h1>
<p>Cerdeña: la isla de los perfumes, olores y colores de una tierra que vive y siente con orgullo todas sus tradiciones; una tierra en la cual el pasado se mezcla con el presente mirando al futuro, el amor por las cosas sencillas de la vida se refleja también en la comida, el momento que nos une a todos: comer, beber, cantar y compartir risas, celebrar la vida, como una familia…los ingredientes de esta cocina vuelven a evocar las tradiciones agro-pastoriles de la isla y celebran el fuerte ligazón que hay entre el hombre y la tierra, algo tan profundo que os marcará para siempre, empezando por los sabores!</p>

<p>Pese a ser es una isla, Cerdeña es famosa por sus platos a base de carne más que de pescado. El pescado se cocina con más frecuencia solo algunas zonas costera (como la península del Sinis) y se puede afirmar que no es la especialidad de la isla, quitando quizás la famosa ‘bottarga di muggine’ (huevos de mar prensados y salados, como un embutido de pescado).</p>

<p>Estos son los platos más típico de la cocina de Cerdeña:</p>

<h4>ENTRANTES</h4>
<p>Los más característicos se componen de salazones (jamón, embutidos, etc.), queso de oveja de Cerdeña, aceitunas y verdura. Otra opción, pero como dijmos secundaria, son los entremés de mar: mejillones, almejas, lapas, cangrejos, palomillas, pulpos, huevos de mújoles (bottarga) y langostas.</p>

<h4>TIPOS DE PASTA Y PAN</h4>
<p><strong>Malloreddus</strong>: se encuentran en toda la Cerdeña, tienen una forma oval muy parecida a una cáscara vacía con el dorso cebrado. Son muy parecidos a los ‘gnocchi di farina’ típicos en casi todas las cocinas regionales italianas. Deliciosos preparados con salsa de tomate, salchicha y mucho queso de oveja rallado (pecorino). <br/>
<strong>Culurgiones</strong>: ravioli cuadrados, rectangulares o circulares rellenos de ricota, queso o patatas y azafrán. <br/>
<strong>Fregola</strong>: caldo de carne con pasta de sémola trabajada a mano con forma de pequeñas bolitas rellenas de ricota. <br/>
<strong>Pan Carasau</strong>: conocido también con el nombre de pan música, se presenta como una ligera hoja de hojaldre; Es el pan más famoso de Cerdeña, buenisimo con queso y embutidos.<br/>
<strong>Pan Frattau</strong>: hecho con el pan carasau mojado en el caldo, sazonado con salsa de tomate y un huevo frito encima cubierto de queso de oveja rallado.<br/>
<strong>Pan Guttiau</strong>: pan carasau sazonado con aceite y sal y calentado en el horno; <br/>
<strong>Pistoccu</strong>: hay que mojarlo antes de servirlo para que no esté demasiado duro.</p>

<h4>CARNE </h4>
<p><strong>Porceddu</strong> (cochinillo asado): cocinado en el asador y aromatizado con hojas de mirto, se sirve caliente y se degusta con un buen vaso de vino tinto de Cerdeña, por supuesto. El mismo proceso se realiza también con la carne de cabra y de oveja. ¡Este es sin duda alguna el plato más típico de Cerdeña! <br/>
<strong>Cordula</strong>: Se utilizan los intestinos de las cabras y las ovejas recién nacidas y se enlazan de manera que vayan formando un recipiente lleno de trozos de estómago del mismo animal. Se cocina con el asador.</p>

<h4>POSTRES</h4>
<p><strong>Sebadas (o seadas)</strong>: la masa sin levadura se trabaja con margarina y se va cubriendo con queso agrio por el limón y la naranja rallados. Tiene una forma circular, se sirve frita poniéndole encima azúcar o miel.<br/>
<strong>Casadinas</strong>: se parecen a unas pequeñas cestas de masa blanda con relleno de queso de oveja, azafrán, azúcar, vainilla y naranja rallada. <br/>
<strong>Papassinas</strong>: tienen una forma romboidal, hechos con masa de sémola fermentada, huevos, nueces, margarina, almendras y a veces glaseados encima. <br/>
<strong>Torrone</strong> (turrón): hecho con miel, nueces, almendras y la clara del huevo. Recomendado por su exquisitez, el más popular se produce en el pueblo de Tonara.</p>

<h4>LICORES</h4>
<p><strong>Filu e ferru</strong> (o abbaardente): Es el aguardiente de Cerdeña. Durante la II guerra mundial se producía ilegalmente y se escondía bajo tierra. Para que no se perdiera la localización de la botella se le amarraba un hilo de hierro que llegaba hasta la superficie y era identificable, de ahí su nombre. <br/>
<strong>Mirto</strong>: sin duda es el licor más popular. Se produce de la planta siempre verde del mirto, de dos metros de altura. Es un proceso de infusión entre una parte alcohólica, el fruto pulposo de la planta y el azúcar. Se sirve frío.</p>

<h4>VINO</h4>
<p>Aunque Cerdeña tiene una tradición vinícola milenaria, sólo en los últimos años ha elevado la calidad de su vino a buenos niveles en el ámbito nacional; entre ellos, seguramente los más famosos son el <strong>CANNONAU</strong> y el <strong>VERMENTINO</strong>.</p>

