        <h1>Porqué Zágara</h1>
<p><strong>Zágara</strong> (nombre italiano de Azahar) es la flor de los cítricos y proviene del árabe al-azahar que significa <strong>flor blanca</strong>. Crece generalmente en zonas como el de <strong>la costa mediterránea</strong>.<br/>
La palabra <strong>Azahar</strong> también tiene la misma raíz de <strong>Azar</strong> que significa <em>suerte</em>.</p>


<p><strong>La Historia del nombre </strong><br/>
<em>"Estábamos buscando un símbolo que podía identificar todo el Mediterráneo y sus gentes y al mismo tiempo que se pudiera englobar en una sola palabra... corta y musical! <br/>
Un día de primavera paseando por las calles y plazas del barrio de Gracia de Barcelona, de repente me llegó a la nariz un olor: recuerdo de mi vida en Sicilia; 
Era <strong>el olor del Azahar</strong> (hay árboles decorativos de naranjos en algunas plazas de este barrio) y entonces lo tuve muy claro, el nombre tenía que ser el de <strong>una flor que existiera en cualquier parte del Mediterráneo</strong>, cuya olor indica primavera y pureza y cuyo fruto sea uno de los ingredientes fundamentales de las cocinas de muchos países e islas bañados por este mar."</em> <br/>
<strong>(Boris, Trinakria Tours)</strong></p>

