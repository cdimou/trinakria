        <h1>Como llegar a Creta</h1>
<p>Llegar a Creta no es difícil, solo hay que buscar pacientemente en Internet y encontraras buenas soluciones. Para buscar vuelos a Creta desde España os aconsejamos usar sobretodo la web de <a href="http://www.vueling.com/" target="blank">Vueling</a>, que es la única compañía que tiene conexiones directas Barcelona-Heraklion.</p>

<p>Nosotros no tramitamos vuelos pero os podemos aconsejar acerca de las mejores combinaciones de vuelos: <a href="<?php echo site_url();?>/contactar">envíanos un mail</a> y te ayudaremos.</p>
  
<p><strong>CÓMO LLEGAR A CRETA EN AVIÓN</strong><p>

<p>Estos enlaces te pueden resultar útiles: 
<ul>
<li><a href="http://www.heraklion-airport.info/" target="blank"><u>Aeropuerto Internacional de Heraklion</u></a></li>
<li><a href="http://www.google.com/url?q=http%3A%2F%2Fwww.chania-airport.com%2F&sa=D&sntz=1&usg=AFQjCNEEmlw9nDLGk94HmsK6qhXkG9GCtA" target="blank"><u>Aeropuerto Internacional de Chania</u></a></li>
</ul>

<p>Para llegar a Creta desde los diferentes aeropuertos españoles <strong>casi siempre hay siempre que hacer escalas en Atenas</strong> y, aunque no es muy cómodo, puede ser una buena ocasión para combinar el viaje visitando la capital griega y luego seguir para Creta.</p>

<p>Tenéis que mirar un vuelo Madrid/Barcelona-Atenas y luego Atenas-Chania o Heraklion. Una muy buena idea es llegar en una de las dos ciudades y salir de la otra.</p>

<p>La compañía que realiza los mejores enlaces desde <strong>Madrid</strong> y <strong>Barcelona</strong> es <a href="http://es.aegeanair.com/Default.aspx" target="blank">Aegean Airlines</a>.</p>

<p>Con <a href="http://www.vueling.com/" target="blank">Vueling</a> se puede volar directos desde Barcelona a Heraklion. Si viajáis desde otra ciudad, se puede escoger la opción de hacer escala en Barcelona.</p>

<p>Con <a href="http://www.ryanair.com/" target="blank">RyanAir</a> es posible llegar a Creta desde varios aeropuertos italianos como Bolonia, Milán Bergamo, Pisa o Roma Ciampino.</p>

<p>También se puede mirar con <a href="http://www.easyjet.com/asp/es/Reserve/index.asp?lang=ES" target="blank">EasyJet</a> que ofrece la posibilidad de realizar conexiones directas:</p>
<ul>
<li>a Heraklion desde <font color="#FF7F00">Berlin Schoenefeld</font>, <font color="#FF7F00">Londres Gatwick</font>, <font color="#FF7F00">Milán Malpensa</font></li>
<li>a Chania desde <font color="#FF7F00">Londres Gatwick</font></li>
</ul>

<p>Finalmente otra posibilidad sería servirse de <a href="http://www.transavia.com/hv/es-ES/inicio" target="blank">Transavia</a> que enlaza varias ciudades francesas con la capital de Creta (Heraklion).</p>
 
<p><strong>CÓMO LLEGAR A CRETA EN BARCO</strong> </p>
 
<p>Podéis coger un vuelo a la capital griega y luego seguir en ferry hasta Creta (4-8 horas, dependiendo si son 'super ferries' o barcos grandes para que quepan los coches). </p>
 
<p>La mejor línea de ferry es la Atenas-Heraklion o Chania y podéis comprar los billetes en <a href="http://www.fantasticgreece.com/ferries/search.aspx" target="blank">Fantastic Greece</a>.</p>


