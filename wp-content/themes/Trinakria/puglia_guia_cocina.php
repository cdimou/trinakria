	<h1>La Cocina en Puglia</h1>
<p>Todo el mundo conoce la mozzarella pero quizás no muchos saben que la mejor mozzarella de Italia (y del mundo!) se encuentra propio en la Puglia! Solo por probar la <em>mozzarella pugliese</em> merece la pena hacer un viaje en esta maravillosa región del sur de Italia! </p>

<p>Al ser un lugar típicamente mediterráneo, en Puglia obviamente se encuentran todos los productos más representativos de estas latitudes del mundo, es decir aceite extravirgen de oliva, pasta, pescado y verduras como berenjenas, pimientas, calabazas, alcachofas, etc. pero para los amantes de la mozzarella aquí el tiempo se para... La burrata es una mozzarella rellena de leche y nata que se utiliza también para la producción de otro tipo que es la "stracciatella" o sea una mozzarella de forma cremosa y fileteada más que redonda. Otros quesos muy buenos son el caciocavallo, la ricotta forte (una ricotta dura que se ralla sobre los platos) o la scamorza. </p>
 
<p><strong>Aquí detallamos los platos más típicos de la cocina de Puglia:</strong></p>

<p>
<strong>Orecchiette alle cime di rapa:</strong> Las "orecchiette" son una pasta fresca casera con la forma de pequeñas orejas. El ingrediente principal es la rapa (el nabo), verdura que se parece a los brocolis. La salsa está hecha por nabo y anchoas. Es una receta muy sencilla y al mismo tiempo riquísima!<br/>
<strong>Riso patate e cozze</strong>: Típica de Bari, es una receta hecha al horno y se prepara poniendo en una bandeja capas de patatas, mejillones y arroz. Claramente hay otros ingredientes que pero es mejor descubrir probando un buen plato!<br/>
<strong>Gnummareddi o Turcinelli</strong>: Son rollitos de vísceras (hígado, pulmón y riñón en tripas) cerrados dentro de los intestinos de corderos o cabritos, del tamaño de unos 5 cm, junto con unas hojas de perejil y semillas de hinojo.<br/>
<strong>Carne al fornello</strong>: Seguramente lo más típico de la comida rápida callejera. Se trata de una tradición antigua de cocinar la carne "al fornello", es decir en pinchos puestos alrededor de la llama (fornello) de manera que se cocine más por el calor que por el contacto con el carbón que en cambio quema la grasa cambia evidentemente el sabor.<br/>
<strong>>Cicoria con purea di fave</strong>: La achicoria (endibia) con puré de habas es otro plato muy sencillo de la cultura campesina de Puglia que resulta muy bueno cuando los productos son frescos. Es un excelente plato de verduras.<br/>
<strong>Focaccia</strong>: Entre un pan y una pizza, la focaccia tipica pugliese se come caliente y fría, como entrante o simplemente como sustituto del pan. Normalmente se le ponen encima tomates cherry.<br/>
<strong>Cozze alla tarantina</strong>: Se trata de una versión pugliese de mejillones en salsa (picante) de tomate.<br/>
<strong>Involtini di melanzane alla salentina</strong>: Rollitos de berenjena preparados con mortadela, queso pecorino y tomate. El resultado es una tapa realmente deliciosa.<br/>
<strong>Frisella</strong>: La frisella es un pan a forma de "Donut" que se moja en agua y luego se condimenta con aceite, sal, tomate y orégano, también se le pueden añadir verduras encurtidas, olivas, rúcula, etc. etc. Muy difundida sobre todo en verano para comer algo fresco! <br/>
</p>

<p>Mención aparte va a una verdadera pasión para el <strong>pescado y marisco crudo</strong> en la ciudad de Bari. Desde siempre (cuando aún el mar no estaba tan contaminado) la gente de Puglia estaba acostumbrada a comer pescado crudo, hoy en día (siguiendo también la boda de los sushi bar en todo el mundo) algunos restaurantes de Bari son verdaderos especialistas y lugares donde comer unas crudités increibles.</p>
 
<p><strong>Entre la grande variedad de dulces de Puglia, destacamos:</strong><br/>
<strong>Pasticciotto</strong>: Es un postre típico de la zona del Salento que consiste en hojaldre relleno de crema pastelera y horneado. <br/>
<strong>Zeppole</strong>: Generalmente preparadas para la fiesta del papa, las "zeppole", de forma circular, son o fritas o cocidas en el horno y luego se les pone por arriba crema blanca o negra (de gianduia, un tipo de chocolate) y nata. </p>

<p><strong>y los mejores vinos de Puglia:</strong><br/>
