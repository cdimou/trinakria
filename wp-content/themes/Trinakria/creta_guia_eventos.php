        <h1>Eventos y fiestas en Creta</h1>

<p>Creta es una isla llena de cultura y eventos, tanto de la tradición popular como festivales más modernos de música, cine y teatro, viajar a Creta para seguir algunos de estos eventos es muy buena idea, sin duda encontrarse con un festival de buena musica tradicional puede ser la guinda del pastel de tu viaje a Creta. </p>

<p>Aquí os dejamos una selección de eventos en Creta para inspirarte. Si conoces algún otro interesante, envíanos un email y lo añadiremos.</p>

<p>Os aconsejamos consultar la agenda cultural de Creta para mayores detalles y para la programación cultural de todo el año.</p>

<p>Estas son algunas de las fiestas y eventos de Creta:</p>


<p><strong>Febrero</strong></p>
<p><a href="#">Carnaval de Rethymno</a>: Se trata de una tradición que en esta ciudad empezó el año 1914 y se ha mantenido viva desde entonces. Es uno de los carnavales más auténticos de Creta, con su desfile de disfraces que empieza en el puerto de Rethymno y el juego de la búsqueda del tesoro perdido que pone a prueba los conocimientos locales.

<p><strong>Abril</strong></p>
<p><a href="#">Fiesta de Agios Georgios</a>. El día 23 Abril en Asi-Gonia, Rethymno, la gente de la zona llevan sus animales al pueblo para que sean bendecidos por el sacerdote. Después ordeñan las cabras y las ovejas, hierven la leche y la sirven a los visitantes.<br/>
<a href="#">Pascua Griega</a>: La pascua griega dura una semana y dos días. Durante este periodo cada día se celebran misas cantadas (según el rito cristiano ortodoxo) en las iglesias , aunque los días más solemnes son los tres últimos: el viernes santo, el sábado santo y el domingo de pascua. O, cómo los griegos lo llaman, el gran viernes, gran sábado y gran domingo.<br/>
<a href="#">Viernes Santo</a>:  En todos los pueblos, por la tarde, se realiza una pequeña procesión por los alrededores de cada iglesia o ermita. Algunos pueblos tienen además sus propias tradiciones. En el pueblo de Sarchos, cerca de Heraklion, por ejemplo, los jóvenes construyen estatuas de Judas que cuelgan en una pila de madera y queman en las dos plazas del pueblo. En Agios Nikolaos, la ciudad principal del este de la isla, se realiza la quema de Judas en el lago de la ciudad.<br/>
<a href="#">Sábado Santo</a>: La gente acude a la iglesia a medianoche y prenden una vela con la llama que enciende el sacerdote y que la multitud distribuye de unos a otros. Esa llama, conocida en Grecia como la luz santa, simboliza la resurrección de Jesucristo y se mantiene encendida durante toda la ceremonia y el paseo hasta casa, donde se deja encima de la mesa para presidir la cena que rompe la ayuna (supuestamente) llevada a cabo durante las semanas anteriores.<br/>
<a href="#">Domingo de Pascua</a>: Es el día de bailar y comer. El plato típico de este alegre día es el cordero asado.</p>

<p><strong>Mayo</strong></p>
<p><a href="#">Fiesta de Anthonero</a>: El día 1 de Mayo la gente local del pueblo de Furnes, Chania, organiza una fiesta donde los visitantes pueden observar como se hace la destilación del agua de flores de cítricos y saborear platos locales.</p>

<p><strong>Junio</strong></p>
<p><a href="#">Cura</a>:  Cada año, a finales de Mayo y principio de Junio, en varios pueblos del interior de Creta se realiza el esquileo de las ovejas. Esto tiene lugar en los ganaderos, las queserías y todo el ritual es en dialecto cretense. Se trata de una tradición que empezó con la civilización minoica y que todavía sigue viva en los pueblos de la isla. Es una labor difícil y por esta razón se necesita la ayuda de toda la familia, amigos y... visitantes, si se prestan! Durante el esquileo no faltan los amigos músicos que se acercan al mitato para tocar alguna que otra canción tradicional a cambio de alguna ración de carne de cabra, queso y vino, que se reparte a continuación a todos los visitantes.<br/>
<a href="#">Fiesta de las cerezas</a>: En el pueblo Karanu, Chania. Fiesta popular con música, platos típicos tradicionales, vino y muchas cerezas...</p>

<p><strong>Julio</strong></p>
<p><a href="#">Yakinthia Festival</a>: De 21 a 25 un festival de música, danza y teatro de diferente países. Se celebra en las afueras del pueblo de Anogia. conocido por su carácter rudo y por ser patria de algunas de las familias de músicos más respetadas de la isla, como los Xilouris.</p>

<p><strong>Agosto</strong></p>
<p><a href="#">Fiesta de la Virgen María</a>:  El día 15 de agosto es la fiesta religiosa más importante del país y en muchos pueblos de la isla se organizan fiestas populares, con música al son del laúd y la lira cretenses y bailes tradicionales que jóvenes y mayores parecen conocer a la perfección. Todo ello transcurre mientras en algún rincón de la plaza se asan los corderos y se reparte el vino y rakí local.</p>

<p><strong>Septiembre</strong></p>
<p><a href="#">La fiesta de la sardina</a>: Durante la primera semana de septiembre se organiza este evento en Nea Chora, Chania, en el cuál el ayuntamiento ofrece sardinas a la brasa y vino de la región gratuitamente a los visitantes. Como uno puede imaginar, la música no falta en esta fiesta.</p>

<p><strong>Octubre</strong></p>
<p><a href="#">Rakokazano</a>: Fiesta de destilación de la uva para la preparación del Raki o la tsikudiá, que son variantes refinadas del orujo blanco. La gente lleva sus uvas al destilador oficial de la localidad y espera horas y horas hasta que la primera Raki está lista. Como no puede ser de otra manera, esa espera pronto se transforma en fiesta improvisada, con música, comida, vino y la Rakide años anteriores.</p>

<p><strong>Noviembre</strong></p>
<p><a href="#">Fiesta de Agios Andonis</a>: El día 17 de Noviembre de los últimos cien años se celebra 'Αγιος Αντώνης (Agio Antonis) en Lakoi, Chania. La fiesta popular se celebra en la plaza “Seli” del pueblo donde en cada <em>cafenío</em> o café hay músicos que tocan música tradicional con violín y laúd. Los cafeníos sirven solo bebidas y tapas.</p>

