<h1>Eventos y fiestas en Puglia</h1>
<p>Que mejor que saber cuales son los mejores eventos o las fiestas tradicionales más importantes de Puglia para <a href="<?php echo site_url();?>/puglia">planificar tu viaje</a>? Viaja a Puglia considerando su programación cultural y tradicional.</p>

<p>Aquí presentamos una pequeña selección de eventos destacados en Puglia para que no te los pierdas. Si conoces alguno interesante, <a href="<?php echo site_url();?>/contactar">envíanos un email</a> y lo añadiremos.</p>

<p>También aconsejamos consultar las web: <strong><a href="http://www.pugliaevents.it/" target="blank">Puglia Events</a></strong> y <strong><a href="http://www.sagreinpuglia.it/" target="blank">Sagre in Puglia</a></strong> para mayores detalles y la programación cultural de todo el año.</p>

<p>Hay varios eventos y festivales en Puglia durante todo el año aunque la actividad se hace más interesante en verano cuando, sobretodo en lugares como el Salento, los pueblos se llenan de oferta cultural y gastronómica al aire libre para los lugareños y los viajeros. A destacar todas las manifestaciones ligadas a la música tradicional de Puglia: la <em>Pizzica Pizzica</em> y la <em>Tarantella</em>.</p>

<p>Los eventos que más nos gustan son:</p>

Febrero
<p><em><a href="">La Semana Santa pugliese</a></em>: como en todo el sur de italia hay varias procesiones, sobre todo el Viernes Santo y el Domingo de Pascua y en casi toda la región podréis asistir a varias manifestaciones religiosas que, como en todo el sur, muchas veces están contorneadas por varios elementos paganos y de la tradición popular. Los pueblos más activos son: Bitonto (el viernes hacen la Processione dell'Addolorata), Conversano (el viernes la <em>Processione del Cristo Nero</em> en la Chiesa di Santa Maria dell’Isola a las 3,00 de la madrugada), Troia (Processione delle 'Catene' a las 10,00 de la mañana en la chiesa di Sant Anna), Alberobello (el viernes, Passione Vivente a las 21,00), Ruvo di Puglia (el Domingo, Processione di Gesù Risorto - Scoppio delle Quarantane a las 9,00), Gallipoli (el Domingo, Scoppio della Caremma a las 12,00 en el casco antiguo) y Francavilla Fontana (el domingo, Processione di Cristo Risorto a las 18,00 en la chiesa immacolata). aparte estos lugares hay muchos más consultables en <strong><a href="http://www.settimanasantainpuglia.it/" target="blank">Settimana Santa in Puglia</a></strong></p>

Agosto
<p><em><a href="#">La Notte della Taranta</a></em>: Es un festival de música popular del Salento : la Pizzica Pizzica y Tarantella. La hacen en Agosto en el pueblo de Melpignano pero recien  también en varios otros pueblos. La atmósfera es mágica: músicos que tocan, gente que baila y una energía muy positiva. Hay que verlo! Más información en <strong><a href="http://www.lanottedellataranta.it/" target="blank">La Notte della Taranta</a></strong></p>
