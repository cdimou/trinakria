        <h1>Eventos y fiestas en Cerdeña</h1>
<p>Conocer los eventos más destacados y las fiestas tradicionales más importantes de Cerdeña hará que tu viaje sea una experiencia irrepetible. <a href="#">Organiza tu viaje</a> a Cerdeña teniendo en cuenta la programación cultural y popular de la isla.</p>

<p>A seguir os presentamos una selección de fiestas tradicionales y eventos importantes que a lo largo del año se realizan en Cerdeña. Si conoces alguno interesante que no está en la lista, <a href="#">envíanos un mail</a> y lo añadiremos.</p>

<p><strong>Febrero</strong><br/>
<p>El Carnaval de Cerdeña es muy famoso en todo el mundo, muchas revistas de todo el mundo como el National Geographic han retratado varias veces los carnavales de los pueblos de Cerdeña. Se trata de tradiciones antiguas que la gente de Cerdeña sabe valorar y conservar de forma sabia, los más famosos son:</p>

<p><a href="#">Sa Sartiglia</a>, Oristano: de origen medieval, este espectáculo es aún hoy, una manifestación ecuestre hecha para entretener y divertir al público. Se trata de un juego a caballo que tiene raíz en las muchas tradiciones españolas de juegos a caballo.<br/>
<a href="#">Los Mamuthones</a>, Mamoiada: si bien no es posible todavía definir exactamente las orígenes de esta fiesta, seguramente las raíces se remontan a la época precristiana. El ritual se ha mantenido intacto a lo largo de siglos; gente con máscaras de madera y campanas en las espaldas, representan una técnica mágica para intervenir, en el paso de invierno a primavera, sobre las fuerzas productivas de la tierra. Ottana, “boes” y “merdules”: “boes” y “merdules” son máscaras de madera, típicas de este pueblo que representan a la vaca y su propietario. Se recitan escenas de un pasado no muy lejano, cuando la gente trabajaba en el campo. Otras fiestas de carnaval interesantes se celebran en Tempio Pausania y Fluminimaggiore.</p>

<p>Un <a href="#">viaje a Cerdeña</a> en la época del carnaval es una experiencia única y una grande inmersión en la cultura popular y las tradiciones de la isla.</p>

<p><strong>Marzo</strong><br/>
<strong>Fiesta de las naranjas</strong> (sagra dell’agrume), Muravera: fiesta de los cítricos y de las naranjas, famosa por su reunión de grupos folclóricos vestidos con trajes típicos y que llegar a Muravera de todos los otros pueblos de Cerdeña para participar a la fiesta. </p>

<p><strong>Abril</strong><br/>
La Semana Santa en Cerdeña es una de las más sentidas de todo el mediterráneo. En muchos pueblos de la isla se realizan procesiones en los días principales de la fiesta religiosa. Un viaje para vivir la pascua de Cerdeña es un viaje para descubrir tradiciones antiguas en un marco de naturaleza exuberante que solo Cerdeña puede ofrecer al viajero.</p>

<p>Aquí os detallamos un posible itinerario de semana santa en Cerdeña a seguir durante un viaje:</p>

<strong>La Semana Santa de l’Alguer</strong>: claramente de origen catalán, cada día de la semana hay una procesión o una celebración, desde el “Dimarts Sant” hasta el “Dissabte Sant”. <br/>
<strong>Su Incontru de Villagrande Strisaili (NU)</strong>: El Domingo de Pascua se representa el encuentro entre Jesús resucitado y la Madonna; dos grupos, uno de hombres y otro de mujeres, hacen dos procesiones por las calles del pueblo para encontrarse finalmente delante de la iglesia. <br/>
<strong>Su Incontru de Oliena</strong>: de origen español, representa el encuentro entre Jesús resucitado y la Madonna, con una procesión de estatuas por las calles del pueblo.<br/>
En el norte de Cerdeña, en el bonito pueblo costero de <strong>Castelsardo</strong>, la semana santa se desarrolla a lo largo de los lugares más emblemáticos del pueblo como son el Castillo de los Doria y la Catedral de San Antonio Abad donde hay rituales muy sentidos y llenos de emoción como el <strong>Lunissanti</strong> (el lunes antes de semana santa), la <strong>Prucissiuni</strong> (el jueves santo) y el <strong>Ilcrivamentu</strong> (El viernes santo, la crucifixión de cristo). La Semana Santa de Castelsardo es de las más antiguas de Cerdeña, es organizada por la cofradía del oratorio de la Santa Cruz y ofrece cantos a cuatro voces de origen pre-gregoriano.</p>

<p><strong>Mayo</strong><br/>
<strong>La cabalgada sarda</strong>, Sassari: es una grande manifestación nacida en 1899 en ocasión de la visita del rey Vittorio Emanuele. En ella participan grupos tradicionales, caballeros y músicos de toda la isla. Además de la reunión, los caballeros se enfrentan en exhibiciones a caballo. <br/>
<strong>1er de Mayo - "Sant’Efisio"</strong>, Cagliari: la procesión, en la que participan miles de devotos con trajes antiguos, parte de Cagliari y pasa por otros pueblos cercanos. <br/>
<strong>Festha Manna</strong>, Porto Torres (3 de mayo): procesión de estatuas y manifestaciones de cultura, deporte y diversión. <br/>
<strong>Sa festa de is bagadios</strong>, Villagrande Strisaili (NU): fiesta de los solteros, hecha para los solteros que buscan pareja...</p>

<p><strong>Junio</strong><br/>
<strong>Fiesta de Santa Caterina</strong>, Orroli, primer domingo de Junio: procesión con trajes típicos en las “traccas”, carros arrastrados por bueyes. <br/>
<strong>Fiesta Mayor de San Vero Milis</strong>: música y bailes típicos de la tradición sarda.</p>

<p><strong>Julio</strong><br/>
<strong>Sagra dell’Ardia</strong>, Sèdilo (OR), primeros 10 días de Julio: una carrera con caballos por un trayecto escabroso. <br/>
<strong>Festa della Madonna del Naufrago</strong>, Villasimius, primeros 10 días de Julio: procesión en el mar con barcos decorados que llegan hasta un punto del mar, donde debajo, se encuentra una estatua de la Virgen para hacer cumplidos florales a cambio de protección.</p>

<p><strong>Agosto</strong><br/>
<strong>Sagra del Redentore</strong>, Nuoro, último domingo de Agosto: miles de personas exhiben y desfilan con sus antiguos y coloridos trajes típicos. <br/>
<strong>Fiesta de San Lorenzo</strong>, Sanluri, 10 agosto: durante toda la semana, el centro del pueblo se llena de paradas con comida típica y además se organiza un juego a caballo, una carrera de origen medieval. <br/>
<strong>Procesión del rio</strong>, Bosa: procesión en barcos a lo largo del río que cruza el pueblo. <br/>
<strong>Fiesta de Santadi</strong>: una pareja se casa según las tradiciones antiguas. Por la mañana hay una reunión con trajes típicos de algunos grupos que bailan y cantan siguiendo las comitivas del novio y de la novia; al final de la reunión se celebra el matrimonio.</p>

<p><strong>Septiembre</strong><br/>
<p>Un mes ideal para organizar un viaje a Cerdeña es sin duda alguna septiembre: la estación veraniega ya ha acabado y con su fin la isla se ha vaciado de visitantes pero el agua del mar todavía es cálida y agradable y la programación de fiestas populares o vinculadas al vino y el aceite en la isla son muchas. Aconsejamos programar tu visita a Cerdeña durante este mes.</p>

<p><strong>Festa del borgo</strong>, Sanluri: Mercadillo de todos los productos artesanales y la comida típica de Cerdeña, En Sanluri durante unos días se recrea la atmósfera histórica del pueblo. <br/>
<strong>Fiesta de la vendimia</strong>, Quartu Sant’Elena, segunda quincena de septiembre: en honor de la Santa Matrona. <br/>
<strong>Balladores y Mudaos</strong>, Ollolai, último domingo: caballeros y caballos en procesión.<br/>
<strong>La corsa degli scalzi</strong>, San Salvatore (OR), primer fin de semana de septiembre: Esta es una espectacular carrera a pies descalzos por los senderos de la península del Sinis (de Cabras a S. Salvatore). Más información:<br/> http://www.corsadegliscalzi.com/<br/>
<strong>Las olimpiadas de los juegos tradicionales</strong>, Villamar y Nureci, finales de septiembre: Es probablemente el evento más esperado para todos los niños de Cerdeña que, vestidos con hábitos tradicionales, se desafían en los juegos ‘callejeros’ del pasado. Algunos de los juegos propuestos son Luna monta, Badruffa, Biccus, Cadiredda santa, Su giogu de su fusti a Brillias, antenatos de los bolos, las canicas o la peonza para citar algunos.</p>

<p><strong>Octubre</strong><br/>
<p>Los viajes de otoño en Cerdeña son un pequeño lujo, todavía encontraréis días de sol en la costa para bañaros en sus playas de ensueño que se os presentarán en toda su fuerza y con muy poca gente y al mismo tiempo, en el interior de la isla, la atmósfera es típicamente otoñal con todas las tradiciones y las fiestas de otoño. Si buscais encontrareis muchas manifestaciones interesantes, aquí os damos alguna idea:</p>

<p><strong>Fiesta de las castañas</strong>, Aritzo, IV domingo.<br/> 
<strong>Fiesta de Santa Vida</strong>, Serrenti, primer lunes: además de la procesión religiosa, se organiza la exposición de todos los productos típicos, conciertos y fuegos artificiales.</p>

<p><strong>Noviembre</strong><br/>
<p>Sanluri, San Martino-11/11: manifestación religiosa y también conciertos, espectáculos y degustación de productos locales.</p>

