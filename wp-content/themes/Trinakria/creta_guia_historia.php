        <h1>La historia de Creta</h1>

	<p>La isla de Creta no es nueva al turismo, de hecho fue pionera en el mediterráneo en la construcción de grande complejos turísticos pero afortunadamente la Creta turística no se acaba con los resorts del norte de la isla sino que sigue por sus pueblos del interior, las playas y las gargantas del sur, sin olvidar su inmenso patrimonio arqueológico y todos los palacios reales que la caracterizan. <a href="<?php echo site_url();?>/creta/rutas">Un viaje a Creta</a> se puede realizar de muchas maneras: siguiendo itinerarios más culturales o más naturales, o hacer una mezcla de todo con su genuina gastronomía como guinda del pastel. Aquí hemos redactado una pequeña guía de viaje de Creta para que podáis empezar con la planificación del viaje.</p>

	
	<p>Creta (en griego Κρήτη / Kriti) es una isla situada en el <font color="#FF7F00"><em>Mar Egeo</em></font>. Es la isla más grande de <font color="#FF7F00"><em>Grecia</em></font> y la quinta en tamaño del <font color="#FF7F00"><em>Mar Mediterráneo</em></font>, después de <a href="http://shinesicily.com/Historia.asp?lang=esp" target="blank"><em>Sicilia</em></a>, <a href="<?php echo site_url();?>/cerdena/guia/historia"><em>Cerdeña</em></a>, <font color="#FF7F00"><em>Córcega</em></font> y <font color="#FF7F00"><em>Chipre</em></font>. También es conocida como <font color="#FF7F00"><em>Candia</em></font> (desde Candidus - Blanco) que le fue dado por los marinos italianos por su tierra tan blanca.</p>

<p>Creta tiene una superficie de 8.300 kilómetros cuadrados, sus costas son de 1.040 kilómetros, y está habitada por unos 600.000 habitantes. Su capital es <font color="#FF7F00"><em>Heraklion</em></font> que se encuentra en el noroeste de la isla y es la ciudad más grande (200.000 habitantes) y otras ciudades importantes son Chania y Rethymno.
</p>

<p>Para leer más ir a <a href="http://viajesacreta.blogspot.com.es/" target="blank">nuestro Blog</a> o en la página de <a href="http://es.wikipedia.org/wiki/Creta" target="blank">Wikipedia</a>.</p> 

<p>Alguna pequeña noción y data de la historia de Creta:</p>

<p class="text-muted"><strong>5000-2500 a.C.</strong></p>
<p>Los primeros asentamientos en Creta son del <font color="#FF7F00"><em>Neolítico</em></font>. A este periodo pertenecen algunas cerámicas y casas que se encontraron.</p>

<p class="text-muted"><strong>2600 a.C.</strong></p>  
<p>Llega inmigración desde el <font color="#FF7F00"><em>Anatolia</em></font> y se empieza a trabajar el <font color="#FF7F00"><em>cobre</em></font>. Esta nueva gente se mezcla con la población ya presente en la isla dando vida a la que será conocida como la <font color="#FF7F00"><em>civilización Minoica</em></font> y que será presente durante más de 1000 años (del 2600 al 1150 a.C. más o menos) que los estudiosos conocen como 4 fases:</p>

<p class="text-muted"><strong>2500-2100 a.C.</strong></p>
<p><font color="#FF7F00"><em>Periodo Pre-Palacial</em></font>: Es una población pacífica que se dedica al comercio con otras poblaciones del Asia Menor.</p>

<p class="text-muted"><strong>2000-1700 a.C.</strong></p> 
<p><font color="#FF7F00"><em>Periodo Proto-Palacial</em></font>: Vienen introducidas muchas cerámicas desde el Anatolia y empiezan las relaciones comerciales con <font color="#FF7F00"><em>Egipto</em></font>. Este es el periodo que se fortalece el poder monárquico y vienen construidos muchos palacios. los más famosos son: <font color="#FF7F00"><em>Knossos</em></font>, <font color="#FF7F00"><em>Malía</em></font>, <font color="#FF7F00"><em>Festo</em></font> e <font color="#FF7F00"><em>Agía Triada</em></font>.</p>

<p class="text-muted"><strong>1600-1450 a.C.</strong></p> 
<p><font color="#FF7F00"><em>Periodo Neo-Palacial</em></font>: Probablemente en este periodo algún terremoto destruye los palacios pero vienen de inmediato reconstruidos, esta vez siguiendo una arquitectura más compleja. También la pintura toma mucha importancia en este periodo. El rey en <font color="#FF7F00"><em>Minosse</em></font> que determina una gran hegemonía en todo el Mar Egeo y una larga red de comercios en todo el Mediterráneo.</p>

<p class="text-muted"><strong>1400-1150 a.C.</strong></p> 
<p><font color="#FF7F00"><em>Periodo Post-Palacial</em></font>: Después de una erupción en la isla de <font color="#FF7F00"><em>Santorini</em></font> que provoca un terremoto en Creta, casi todo viene destruido, solo Knossos se salva, aunque para unos 50 años más. Empieza el periodo de grecización de Creta, llega el pueblo micénico que había ya invadido el <font color="#FF7F00"><em>Peloponeso</em></font> y se instala en la isla.</p>

<p class="text-muted"><strong>67 a.C.</strong></p> 
<p>Creta entra en el <font color="#FF7F00"><em>imperio romano</em></font> y llega el cristianismo. Parece San Paolo haya viajado a esta isla.</p>

<p class="text-muted"><strong>395 d.C.</strong></p>
<p>Después del imperio romano se transforma en una diócesis de <font color="#FF7F00"><em>Bizancio</em></font>.</p>

<p class="text-muted"><strong>824</strong></p> 
<p>40 embarcaciones <font color="#FF7F00"><em>piratas árabes</em></font> llegan a Creta y la ocupan hasta el 961.</p>

<p class="text-muted"><strong>963</strong></p>
<p>Bizancio vuelve triunfante y mantendrá bajo su dominación la isla hasta el 1204.</p>

<p class="text-muted"><strong>1210</strong></p>
<p>Al final de la cuarta cruzada, <font color="#FF7F00"><em>Constantinopla</em></font> se ve obligada a ceder Creta a la <font color="#FF7F00"><em>república de Venecia</em></font> que lo convierte en una base estratégica para sus comercios en oriente. La convivencia de grupos católicos y ortodoxos genera varias luchas de resistencia de estos últimos, aunque en la mitad del siglo XV empieza una convivencia pacífica que dará lugar a un periodo muy rico para las artes. La ciudad de <font color="#FF7F00"><em>Heraklion</em></font> todavía conserva varios ejemplos arquitectónicos de este periodo veneciano, empezando por los 2 leones del puerto. En este periodo nace en la isla: <font color="#FF7F00"><em>Domenico Theotokópoulos</em></font>, gran pintor, muerto en España, mejor conocido como <font color="#FF7F00"><em>'El Greco'</em></font>.</p>

<p class="text-muted"><strong>1641</strong></p> 
<p>Empieza una larga lucha de la flota turca a las ciudades de Creta, que termina en 1669 con la toma de posesión de la isla. El dominio musulmán fue duro y altamente discriminatoria contra la población local, a menudo obligados a convertirse al Islam por la fuerza. Por lo tanto, la resistencia y las luchas de la gente ha sido continua. Especialmente durante el siglo XIX, han habido varias rebeliones por parte de la población local que sólo al principio del siglo XX han comenzado a alcanzar algunos resultados. Entre otras cosas, hay que señalar que la posición de Creta en el Mediterráneo ha sido siempre muy atractiva para muchas potencias europeas, que a menudo han contribuido a las distintas luchas con la esperanza de la posesión.</p>

<p class="text-muted"><strong>1898</strong></p>
<p>Acaba la <font color="#FF7F00"><em>dominación otomana</em></font> a través de la intervención de los ejércitos europeos y empieza un período de autogobierno.</p>

<p class="text-muted"><strong>13 de octubre de 1913</strong></p> 
<p>Creta viene anexa a la Grecia.</p>

<p class="text-muted"><strong>1922</strong></p> 
<p>Los turcos derrotaron de nuevo a Grecia y por la isla esto significa tener que aceptar 13.000 refugiados griegos de Asia Menor y la expulsión de 11.000 cretenses turcos que aún viven en <font color="#FF7F00"><em>Smyrna</em></font>, como si fueran cretenses.</p>

<p class="text-muted"><strong>1941</strong></p> 
<p>Dura y sangrienta batalla contra los alemanes durante la <font color="#FF7F00"><em>Segunda Guerra Mundial</em></font>, que finalmente ocupan la isla, aunque los cretenses organizan rápido la resistencia.</p>

<p class="text-muted"><strong>1945</strong></p> 
<p>Creta vuelve a ser libre pero está destruida y ha perdido muchos hombres y muchos pueblos han desaparecido por los bombardeos. Hoy en día, el <font color="#FF7F00"><em>turismo</em></font> y la <font color="#FF7F00"><em>agricultura</em></font> son su mayor riqueza y la población es en realidad un <font color="#FF7F00"><em>crisol de razas</em></font> muy diferentes.</p>



