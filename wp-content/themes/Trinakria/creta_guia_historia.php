        <h1>La historia de Creta</h1>

<p>La isla de Creta no es nueva al turismo, de hecho fue pionera en el mediterráneo en la construcción de grande complejos turísticos pero afortunadamente la Creta turística no se acaba con los resorts del norte de la isla sino que sigue por sus pueblos del interior, las playas y las gargantas del sur, sin olvidar su inmenso patrimonio arqueológico y todos los palacios reales que la caracterizan. <a href="#">Un viaje a Creta</a> se puede realizar de muchas maneras: siguiendo itinerarios más culturales o más naturales, o hacer una mezcla de todo con su genuina gastronomía como guinda del pastel. Aquí hemos redactado una pequeña guía de viaje de Creta para que podáis empezar con la planificación del viaje.</p>

<p>Creta (en griego Κρήτη / Kriti) es una isla situada en el <a href="#"><em>Mar Egeo</em></a>. Es la isla más grande de <a href="#"><em>Grecia</em></a> y la quinta en tamaño del <a href="#"><em>Mar Mediterráneo</em></a>, después de <a href="#"><em>Sicilia</em></a>, <a href="#"><em>Cerdeña</em></a>, <a href="#"><em>Córcega</em></a> y <a href="#"><em>Chipre</em></a>. También es conocida como <a href="#"><em>Candia</em></a> (desde Candidus - Blanco) que le fue dado por los marinos italianos por su tierra tan blanca.</p>

<p>Creta tiene una superficie de 8.300 kilómetros cuadrados, sus costas son de 1.040 kilómetros, y está habitada por unos 600.000 habitantes. Su capital es <a href="#"><em>Heraklion</em></a> que se encuentra en el noroeste de la isla y es la ciudad más grande (200.000 habitantes) y otras ciudades importantes son Chania y Rethymno.
</p>

<p>Para leer más ir a <a href="<?php echo site_url();?>/blog">nuestro Blog</a> o en la página de <a href="http://es.wikipedia.org/wiki/Creta">Wikipedia</a>.</p> 

<p>Alguna pequeña noción y data de la historia de Creta:</p>

<p class="text-muted"><strong>5000-2500 a.C.</strong></p>
<p>Los primeros asentamientos en Creta son del <a href="#"><em>Neolítico</em></a>. A este periodo pertenecen algunas cerámicas y casas que se encontraron.</p>

<p class="text-muted"><strong>2600 a.C.</strong></p>  
<p>Llega inmigración desde el <a href="#"><em>Anatolia</em></a> y se empieza a trabajar el <a href="#"><em>cobre</em></a>. Esta nueva gente se mezcla con la población ya presente en la isla dando vida a la que será conocida como la <a href="#"><em>civilización Minoica</em></a> y que será presente durante más de 1000 años (del 2600 al 1150 a.C. más o menos) que los estudiosos conocen como 4 fases:</p>

<p class="text-muted"><strong>2500-2100 a.C.</strong></p>
<p><a href="#"><em>Periodo Pre-Palacial</em></a>: Es una población pacífica que se dedica al comercio con otras poblaciones del Asia Menor.</p>

<p class="text-muted"><strong>2000-1700 a.C.</strong></p> 
<p><a href="#"><em>Periodo Proto-Palacial</em></a>: Vienen introducidas muchas cerámicas desde el Anatolia y empiezan las relaciones comerciales con <a href="#"><em>Egipto</em></a>. Este es el periodo que se fortalece el poder monárquico y vienen construidos muchos palacios. los más famosos son: <a href="#"><em>Knossos</em></a>, <a href="#"><em>Malía</em></a>, <a href="#"><em>Festo</em></a> e <a href="#"><em>Agía Triada</em></a>.</p>

<p class="text-muted"><strong>1600-1450 a.C.</strong></p> 
<p><a href="#"><em>Periodo Neo-Palacial</em></a>: Probablemente en este periodo algún terremoto destruye los palacios pero vienen de inmediato reconstruidos, esta vez siguiendo una arquitectura más compleja. También la pintura toma mucha importancia en este periodo. El rey en <a href="#"><em>Minosse</em></a> que determina una gran hegemonía en todo el Mar Egeo y una larga red de comercios en todo el Mediterráneo.</p>

<p class="text-muted"><strong>1400-1150 a.C.</strong></p> 
<p><a href="#"><em>Periodo Post-Palacial</em></a>: Después de una erupción en la isla de <a href="#"><em>Santorini</em></a> que provoca un terremoto en Creta, casi todo viene destruido, solo Knossos se salva, aunque para unos 50 años más. Empieza el periodo de grecización de Creta, llega el pueblo micénico que había ya invadido el <a href="#"><em>Peloponeso</em></a> y se instala en la isla.</p>

<p class="text-muted"><strong>67 a.C.</strong></p> 
<p>Creta entra en el <a href="#"><em>imperio romano</em></a> y llega el cristianismo. Parece San Paolo haya viajado a esta isla.</p>

<p class="text-muted"><strong>395 d.C.</strong></p>
<p>Después del imperio romano se transforma en una diócesis de <a href="#"><em>Bizancio</em></a>.</p>

<p class="text-muted"><strong>824</strong></p> 
<p>40 embarcaciones <a href="#"><em>piratas árabes</em></a> llegan a Creta y la ocupan hasta el 961.</p>

<p class="text-muted"><strong>963</strong></p>
<p>Bizancio vuelve triunfante y mantendrá bajo su dominación la isla hasta el 1204.</p>

<p class="text-muted"><strong>1210</strong></p>
<p>Al final de la cuarta cruzada, <a href="#"><em>Constantinopla</em></a> se ve obligada a ceder Creta a la <a href="#"><em>república de Venecia</em></a> que lo convierte en una base estratégica para sus comercios en oriente. La convivencia de grupos católicos y ortodoxos genera varias luchas de resistencia de estos últimos, aunque en la mitad del siglo XV empieza una convivencia pacífica que dará lugar a un periodo muy rico para las artes. La ciudad de <a href="#"><em>Heraklion</em></a> todavía conserva varios ejemplos arquitectónicos de este periodo veneciano, empezando por los 2 leones del puerto. En este periodo nace en la isla: <a href="#"><em>Domenico Theotokópoulos</em></a>, gran pintor, muerto en España, mejor conocido como <a href="#"><em>'El Greco'</em></a>.</p>

<p class="text-muted"><strong>1641</strong></p> 
<p>Empieza una larga lucha de la flota turca a las ciudades de Creta, que termina en 1669 con la toma de posesión de la isla. El dominio musulmán fue duro y altamente discriminatoria contra la población local, a menudo obligados a convertirse al Islam por la fuerza. Por lo tanto, la resistencia y las luchas de la gente ha sido continua. Especialmente durante el siglo XIX, han habido varias rebeliones por parte de la población local que sólo al principio del siglo XX han comenzado a alcanzar algunos resultados. Entre otras cosas, hay que señalar que la posición de Creta en el Mediterráneo ha sido siempre muy atractiva para muchas potencias europeas, que a menudo han contribuido a las distintas luchas con la esperanza de la posesión.</p>

<p class="text-muted"><strong>1898</strong></p>
<p>Acaba la <a href="#"><em>dominación otomana</em></a> a través de la intervención de los ejércitos europeos y empieza un período de autogobierno.</p>

<p class="text-muted"><strong>13 de octubre de 1913</strong></p> 
<p>Creta viene anexa a la Grecia.</p>

<p class="text-muted"><strong>1922</strong></p> 
<p>Los turcos derrotaron de nuevo a Grecia y por la isla esto significa tener que aceptar 13.000 refugiados griegos de Asia Menor y la expulsión de 11.000 cretenses turcos que aún viven en <a href="#"><em>Smyrna</em></a>, como si fueran cretenses.</p>

<p class="text-muted"><strong>1941</strong></p> 
<p>Dura y sangrienta batalla contra los alemanes durante la <a href="#"><em>Segunda Guerra Mundial</em></a>, que finalmente ocupan la isla, aunque los cretenses organizan rápido la resistencia.</p>

<p class="text-muted"><strong>1945</strong></p> 
<p>Creta vuelve a ser libre pero está destruida y ha perdido muchos hombres y muchos pueblos han desaparecido por los bombardeos. Hoy en día, el <a href="#"><em>turismo</em></a> y la <a href="#"><em>agricultura</em></a> son su mayor riqueza y la población es en realidad un <a href="#"><em>crisol de razas</em></a> muy diferentes.</p>



